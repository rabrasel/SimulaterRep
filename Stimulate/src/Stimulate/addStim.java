    public void orderArray(stimData sd){
        stimDataControler sdc = new stimDataControler("temp");
        int x = 1;
    
        for (int i = 0; i < global.myStimData.stimArray.size(); i++){
            if(x == sd.location){
                sdc.addStimData(sd.getLocation(), sd.getTime(), sd.getFileName(), sd.getEvent());
                x++;
            }
            
//            sdTemp.setLocation(x);
//            sdTemp.setFileName(global.myStimData.stimArray.get(i).getFileName());
//            sdTemp.setTime(global.myStimData.stimArray.get(i).getTime());
//            sdTemp.setEvent(global.myStimData.stimArray.get(i).getEvent());

            sdc.addStimData(x, global.myStimData.stimArray.get(i).getTime(), 
                    global.myStimData.stimArray.get(i).getFileName(), 
                    global.myStimData.stimArray.get(i).getEvent());
            
            x++;
            
        }

        global.myStimData.stimArray.clear();
        global.myStimData.stimArray = sdc.stimArray;
    }
