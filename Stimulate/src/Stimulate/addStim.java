    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(isEdit){
            stimData sd2;
            sd2 = new stimData(Integer.parseInt(locTextField.getText()), 
                    Double.parseDouble(timeTextField.getText()), 
                    fileTextField.getText(),
                    Integer.parseInt(eventTextField.getText()));
            if(sd2.location != sd.location){
                global.myStimData.stimArray.remove(selected);
                orderArray(sd2);
            } else {
                global.myStimData.stimArray.set(selected, sd2);
            }
        } else {
            global.myStimData.addStimData(Integer.parseInt(locTextField.getText()), 
                    Double.parseDouble(timeTextField.getText()), 
                    fileTextField.getText(),
                    Integer.parseInt(eventTextField.getText()));
        }

        isEdit=false;
        saveCanceled = false;
        stimulatForm.fillTable();
        dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

